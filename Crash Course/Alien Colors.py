alien_color = ['green', 'yellow', 'red', 'purple']

for alien in alien_color:
    if alien == 'green':
        print('You just earned 5 points!')

    elif alien == 'yellow':
        print('You just earned 10 points!')

    elif alien == 'red':
        print('You just earned 15 points!')

    elif alien == 'pink':
        print('You earned 1,000,000 points!')

    else:
        print('You just earned 50 points!')
